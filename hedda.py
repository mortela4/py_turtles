# Oppgave:
# skriv "HEDDA" med turtle!
# =========================
# Eksempel-bokstav:
#
# 100 -   |      |
#         |      |
# 50  -   |------|
#         |      |
# 0   -   |      |

import turtle

# Step 2: Create a new turtle. We'll call it "hedda"
hedda = turtle.Turtle()

# Write HEDDA ...
hedda.forward(50)

# Step 4: We're done!
turtle.done()
